    private void tableProductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableProductosMouseClicked
        // TODO add your handling code here:
        int fila = tableProductos.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione una fila");
        }
        else
        {
            String codigo = (tableProductos.getValueAt(fila, 0).toString());
            String nom = tableProductos.getValueAt(fila, 1).toString();
            String precio = tableProductos.getValueAt(fila, 2).toString();
            String stock = tableProductos.getValueAt(fila, 3).toString();
            
            txtCode.setText(codigo);
            txtNom.setText(nom);
            txtPrecio.setText(precio);
            txtStock.setText(stock);
            id = codigo;
        }
    }//GEN-LAST:event_tableProductosMouseClicked

